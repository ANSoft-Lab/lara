	<h1>Рефинансирование</h1>
	<p>Ипотека под жилье берется на длительный срок. Заемщик по объективным причинам, часто от него не зависящим, может испытывать
		финансовые&nbsp;проблемы, возникшие уже после ее оформления. Достойным выходом из сложной финансовой ситуации может стать рефинансирование ипотеки.&nbsp;Рефинансированием в широком смысле является перекредитование, то есть погашение кредита в одном банке путем взятия кредита в другом.</p>
	<p>Прибегая к услуге рефинансирования, заемщик преследует следующие цели:</p>
	<ul>
		<li>Увеличение срока выплаты кредита;</li>
		<li>Уменьшение суммы ежемесячного платежа;</li>
		<li>Снижение процентной ставки;</li>
		<li>Смена валюты кредита (с иностранной валюты на рублевую и наоборот).</li>
	</ul>
	<h3>Кому выгодна услуга рефинансирования</h3>
	<p>Она бесспорно выгодна финансовой организации, предоставляющей эту услугу, и заемщику.</p>
	<p>Возможности рефинансирования следует иметь в виду заемщикам:</p>
	<ul>
		<li>Попавшим в затруднительное материальное положение;</li>
		<li>Имеющим несколько займов и желающих объединить их в один.</li>
	</ul>
	<p><b>Для банка же выгода заключается в:</b></p>
	<ul>
		<li>Выборе кредита для рефинансирования, соответствующего его собственным требованиям;</li>
		<li>Заработке на процентах, получаемых с нового клиента;</li>
		<li>В возможном обращении нового клиента в будущем за другими банковскими услугами.</li>
	</ul>
