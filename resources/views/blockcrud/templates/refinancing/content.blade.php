	<p>Наш сервис удобен тем, что мы работаем почти полностью в дистанционном формате, позволяющем клиенту сэкономить время. По любым предварительным вопросам, связанным с перекредитованием ипотеки, заемщик может обратиться к сотрудникам компании через службу поддержки.</p>
