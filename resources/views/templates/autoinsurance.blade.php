              <h3 class="accordion__title">8. Откуда берется КБМ?</h3>
              <div class="accordion__content">
                <p>Коэффициент бонус-малус (КБМ) — коэффициент, который присваивается каждому водителю в  зависимости от наличия или отсутствия страхового возмещения, осуществленного страховщиками (страховой компанией)  в период, с 1 апреля предыдущего года до 31 марта включительно следующего за ним года. Говоря простым языком,  это коэффициент скидки за безаварийную езду. Коэффициент может быть как понижающий (за безаварийный год), так и повышающий (если в течение года были страховые случаи по вине водителя) </p>
                <p>Изначальный класс, который присваивается водителю 1, а далее меняется каждый год. За каждый год безаварийной езды присваивается скидка 5%. Максимально возможная скидка - 50% (за 10 лет безаварийной езды)</p>
                <p>Нужно обратить внимание, что КБМ (скидка) будет присваиваться только водителям, вписанным в полис ОСАГО.</p>
                <p>Для полисов ОСАГО с неограниченным кол-вом допущенных КБМ будет равен всегда 1 (исключение только ОСАГО для юридических лиц, там КБМ присваивается авто)</p>
