                <p><b>Первый способ</b> проверки проводят по серии и номеру полиса ОСАГО. Осуществить проверку можно и для бумажной версии, и для Е-ОСАГО. После ввода нужных сведений вы получите ответ: «находится у Страхователя» — причин для беспокойства нет, вы купили настоящий полис. Если вариант ответа будет иным, то вашей страховки в базе нет, или она украдена, утеряна, недействительна. При покупке цифровой версии документа не передавайте никому свои личные данные, заполняйте форму самостоятельно. Это защитит вас от мошенничества.</p>
                <p><b>Второй способ</b> — проверить ОСАГО по базе РСА с указанием даты оформления страховки и номера документа. Предполагает более глубокую проверку. В отличие от первого варианта, который выдает информацию о статусе полиса, вам предоставят сведения о застрахованной машине: государственный номер, номер кузова или VIN-номер. Помимо серии и номера страховки в проверочную форму необходимо внести дату, на которую вы запрашиваете данные.</p>
                <p><b>Третий способ</b> — это возможность проверить ОСАГО по госномеру, VIN автомобиля. Дает максимальный объем информации о договоре ОСАГО. Форма запроса включает ввод данных автомобиля: госномера, VIN-номера или номера кузова. Вся информация запрашивается на установленную дату. Выбрав этот вариант, Страхователь получит информацию о Страховой компании, которая выдала бланк, номер страховки и тип полиса (есть ли в нем ограничения по допущенным к управлению лицам).</p>
                <p><b>Как самостоятельно можно проверить подлинность полиса КАСКО?</b></p>
                <p>Если в ОСАГО уже давно действует единая база всех страховых бумаг, то в КАСКО такая практика находится на стадии своего становления. Однако уже сегодня можете проверить подлинность полиса КАСКО по номеру. Есть два варианта пути:</p>
                <p>Посетить официальный сайт страховщика или РСА. На нем есть калькулятор, позволяющий рассчитать страховку, а также проверка полисов.</p>
                <p>Позвонить на горячую линию выбранной фирмы, предъявить серию и номер полиса и дождаться ответа.</p>
                <p>Если решили воспользоваться интернет-порталом, то сможете увидеть не только статус, но и дату оформления договора, примечание. Позвонить страховщику можно и в том случае, если полис страхования был утерян или на нем невозможно прочитать определенную информацию. В этом случае вам укажут все необходимые номера.</p>
                <p>Обратите внимание, что большинство компаний при оформлении КАСКО предлагают в распоряжение личный онлайн-кабинет. С его помощью вы не только сможете отслеживать основную информацию по своему полису, но и не пропустите время его продления. Пароль доступа возьмите у своего страховщика.</p>
                <p>Несмотря на то что в РСА проверка полисов КАСКО не проводится, вы всегда можете узнать о подлинности своего документа другим путем. В настоящей страховой компании вам выдадут оригинал полиса и дополнят его правилами страхования, о внесении платежа, актами осмотра. Эти документы должны быть сохранены на период действия договора.</p>
